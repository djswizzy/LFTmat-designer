export interface BitmapPixel {
  x: number; // 0 .. width-1, left to right
  y: number; // 0 .. height-1, top to bottom
}

/** Return the lit pixels for a string laid out on a 5x7 grid with 1px gaps. */
export function layoutText(text: string, kerning = 1): BitmapPixel[] {
  const pixels: BitmapPixel[] = [];
  let cursor = 0;
  for (const ch of text) {
    // Fall back to the opposite-case glyph if a particular case is missing
    // (covers any future symbol we forget to author both cases for).
    const glyph =
      FONT[ch] ?? FONT[ch.toUpperCase()] ?? FONT[ch.toLowerCase()];
    if (!glyph) {
      cursor += FONT_WIDTH + kerning;
      continue;
    }
    for (let y = 0; y < FONT_HEIGHT; y++) {
      const row = glyph[y];
      for (let x = 0; x < FONT_WIDTH; x++) {
        if (row[x] === "#") pixels.push({ x: cursor + x, y });
      }
    }
    cursor += FONT_WIDTH + kerning;
  }
  return pixels;
}

/** Width of a string in pixels for a given kerning. */
export function measureText(text: string, kerning = 1): number {
  if (text.length === 0) return 0;
  return text.length * FONT_WIDTH + (text.length - 1) * kerning;
}
